
package kkgreat;

/**
 * 書類データ 新規・編集用　生保分選択リスト用クラス
 * 
 */
public class SeihoList {
    private String seiho;
    private String hokengaisha;

    /**
     * @return the seiho
     */
    public String getSeiho() {
        return seiho;
    }

    /**
     * @param seiho the seiho to set
     */
    public void setSeiho(String seiho) {
        this.seiho = seiho;
    }

    /**
     * @return the hokengaisha
     */
    public String getHokengaisha() {
        return hokengaisha;
    }

    /**
     * @param hokengaisha the hokengaisha to set
     */
    public void setHokengaisha(String hokengaisha) {
        this.hokengaisha = hokengaisha;
    }
    
    
}
